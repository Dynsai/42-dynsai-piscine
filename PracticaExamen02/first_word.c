#include <unistd.h>
int	main(int argc, char *str[])
{
	if (argc == 2)
	{
		int     i = 0;

        	while ((str[1][i] == ' ') || (str[1][i] == '\t')) 
			i++;

        	while (str[1][i] && str[1][i] != ' ' && str[1][i] != '\t')
        	{
                	write (1, &str[1][i], 1);
                	i++;
        	}
        	write (1, "\n", 1);
	}
	return (0);
}
